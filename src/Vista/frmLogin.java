    private void btnSesionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSesionActionPerformed
        try {
            Login L = new Login();
            L.setUsuario(txtUser.getText());
            L.setContrasena(txtPassword.getText());
            if(txtUser.getText().equals("") && txtPassword.getText().equals(""))
            {
                lblMensaje.setText("Ingrese un usuario y contrase침a");
            }
            
             if( conLog.Validar(L.getUsuario(), L.getContrasena())){
                 rSPanelsSlider1.setPanelSlider(1,pnlCargando, RSPanelsSlider.DIRECT.RIGHT);
                 new Thread(new Runnable() {
                     @Override
                     public void run() {
                         try {
                             Thread.sleep(500);
                             dispose();
                             new frmInicio().setVisible(true);
                         } catch (InterruptedException ex) {
                             Logger.getLogger(frmLogin.class.getName()).log(Level.SEVERE, null, ex);
                         }
                     }
                 }).start();
             }
             else
             {
               lblMensaje.setText("Error Revise Usuario o Contrase침a");
             }
              
        } catch (SQLException ex) {
            
            Logger.getLogger(frmLogin.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_btnSesionActionPerformed

